pop = 7
if (2 > pop):
   print("2 is bigger than pop")
#    the elif condition, adds another requirement to it if the first one in the if
#  condition is not met,just like "if this doesnt work ,try this" it wont b printed if the first condition is met
elif 2 == pop:
   print("2 is not equal to pop")
#    the else condition works when the first two requirements are not met and wont
#  b printed if any other before it is validated
else:
   print("2 is less than pop")

